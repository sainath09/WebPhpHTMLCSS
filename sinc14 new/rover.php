 <div id="home-article-" class="panel-panel panel-article"><div class="panel-pane pane-node" >
  
       
        
        <h2>A group of astronauts have landed on a mysterious land.
Brace yourself for exploring the moon to find new things there .........
        
         </h2>
        
        <hr>
        
       <h3>Problem Statement</h3>
       
       
       <h4>Design and build a wired or wireless robot, which acts like a rover for the astronauts, which helps them to explore the moon, on which they have landed.
The robot should be capable of doing tasks similar to a rover (picking objects, collecting samples etc.).
       </h4>
       
       
       <h3>Rover Specifications</h3>
       <h4>
       •	The weight of the rover must not exceed more than 5kgs<br>
•	The rover must not exceed the dimensions of 30*30*30 cm (l*b*h)<br>
•	The rover may exceed the given dimensions after its starting, if required<br> 
•	The remote control used to control the robot manually is not included in this size of the rover<br>
•	The potential difference between any two terminals should not exceed 18V<br>
•	Rovers are also allowed to have wireless control<br>
•	Bonus points for rovers capable of detecting the presence of water or collecting water sample<br>
        </h4>



<h3>Arena Specifications:</h3>
<h4>The unknown planet region will contain different type’s unknown terrains, ups and downs and many more. The arena will be spread in an area of around 3*3 meters. This region will contain a lot of obstacles of various sizes which the robot has to evade.
</h4>

<h4>The region will have a spaceship from which the rover will start. The rover will be placed in a platform inside the spaceship at a height of 50cm (from Ground surface). The dimensions of platform will be 35cm x 35cm. The robot can be placed anywhere in the platform but it should fit in the platform before starting. Spaceship and moon’s surface will be connected by an inclined plane at 30 degrees.</h4>



<h3>Rules & Regulations:</h3> 

<h4>•Maximum of four students per team<br>
•	The team should be registered to be eligible<br>
•	The members can be from different colleges<br>
•	Everyone should produce a valid ID card issued by their institution<br>
•	Participants are not allowed to construct rovers with Lego kits<br>
•	Any mischief in controlling or causing damage to other participants’ rovers will lead to serious actions and disqualification<br>
•	Absence of the participant during their trial will be considered as disqualified<br>
•	Use of IC engines is prohibited<br>
•	Rovers separating into two or more independent parts during the event will be disqualified<br>
•	Any damage to the arena by the participants will lead to disqualification<br>
•	Participants are allowed to participate only once with one rover<br>
•	The organizers are not responsible for any sort of damage to the robot<br>
•	Rules & Regulations are bound to changes. The organizers’ decision will be final and abiding<br>
•	Misbehavior of the participants (or) argument with organizers will lead to disqualification<br>
•	The participants are requested to visit the site regularly for any changes in the rules<br>

</h4>

</h4>
