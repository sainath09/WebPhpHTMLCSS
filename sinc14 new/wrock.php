        <h2>“Rocketry is the enabler of all space technologies particularly satellites, many of which impact people's everyday lives in almost countless ways.” </h2>
        
        
       <h4>Scientifically, rocketry has opened a window on the universe, allowing the launch of space probes to explore the solar system and space-based telescopes to obtain a clearer view of the rest of the universe.
       </h4>
       
       <h4>So let us start from the basics. This is the stage for you to express your talent and creativity in building water rocket.</h4>
        
        <h3>Problem Statement:</h3>
        <h4>  The participants are required to design and build a water rocket (single stage/multi stage). The winner will be decided on the basis of the rules mentioned below.</h4>
<h3>Guidelines:</h3>
<h4>• Only plastic soft drink bottles are to be used for the rocket body<br>
• The water rocket must use only compressed ambient atmospheric air as its source of energy. Only tap water shall be used in the rocket, and the water shall be provided by the organizers. Water brought by the team shall not be allowed in the rockets<br>
• No chemical or aerated water will be added for launching<br>
• The amount of water to be filled in the rocket body is left to the choice of the team<br>
• Provision for deployment of a small payload with a parachute will fetch bonus points<br>
• No component or a body part of the rocket should get detached from the main frame during the flight of the rocket except for the case of payload. If any other part gets detached then the rocket will be disqualified
<br>

</h4>
<h3>Rules & Regulations : </h3>
<h4>•	Maximum of 4 members per team<br>
•<b> Eligibility:</b> 8th grade & above (schools) and college students<br>
• The angle at which the rocket is launched, the amount of water to be filled in the rocket body and the pressure inside the rocket can be suggested by the team<br>
• The points earned by the team will depend on the range of flight, leak-free rockets, successful design & deployment of payload with parachute (if any)<br>
• <b><u>Deployment of payload is a compulsory task for college students</u></b><br>
• The range of flight is the distance between the point of launch and the point of first impact with the ground after launch<br>
• Only one attempt is allowed<br>
• Pressure pump will be provided<br>
Separate category of judging for school students & college students. Organizers’ decision is final. 

</h4>

